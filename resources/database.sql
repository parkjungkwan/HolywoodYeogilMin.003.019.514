	ATTR_NAME 		VARCHAR(45) NOT NULL,
	ATTR_ENAME 		VARCHAR(50) NOT NULL,
	ATTR_LAT 		VARCHAR(50) NOT NULL,
	ATTR_LNG 		VARCHAR(50) NOT NULL,
