CREATE TABLE CITY (
	CITY_SEQ 		INT(11) NOT NULL AUTO_INCREMENT,
	CITY_NAME 		VARCHAR(30) NOT NULL,
	CITY_ENAME 		VARCHAR(30) NOT NULL,
	CITY_LAT 		VARCHAR(30) NOT NULL,
	CITY_LNG 		VARCHAR(30) NOT NULL,
	CITY_DNAME 		VARCHAR(250) NOT NULL,
	COUNTRY_ENAME 	VARCHAR(50) NOT NULL,
	PRIMARY KEY (CITY_SEQ)
)ENGINE=InnoDB DEFAULT CHARSET=utf8;

CREATE TABLE CONTINENT
(
    CONTINENT_SEQ    INT            NOT NULL    AUTO_INCREMENT, 
    CONTINENET_NAME  VARCHAR(50)    NOT NULL,
    PRIMARY KEY (CONTINENT_SEQ)
)ENGINE=InnoDB DEFAULT CHARSET=utf8;

CREATE TABLE COUNTRY
(
    COUNTRY_SEQ  		 INT            NOT NULL    AUTO_INCREMENT,
    COUNTRY_NAME 		 VARCHAR(50)    NOT NULL, 
    COUNTRY_CODE 		 VARCHAR(50)    NULL DEFAULT NULL,
    COUNTRY_ENAME		 VARCHAR(50) 	NOT NULL,
    CONTINENT_NAME 	 VARCHAR(50)    NOT NULL, 
    COUNTRY_LAT		 DOUBLE   		NOT NULL, 
    COUNTRY_LNG 		 DOUBLE   		NOT NULL, 
    COUNTRY_FLAG 		 VARCHAR(200)    NULL DEFAULT NULL, 
    PRIMARY KEY (COUNTRY_SEQ)
)ENGINE=InnoDB DEFAULT CHARSET=utf8;

CREATE TABLE HOTEL (
	HOTEL_SEQ 	INT			NOT NULL AUTO_INCREMENT,
	HOTEL_NAME 		VARCHAR(100) NULL DEFAULT NULL,
	PRICE				VARCHAR(50) NULL DEFAULT NULL,
	ROOM_TYPE		VARCHAR(50) NULL DEFAULT NULL,
	NOTICE			VARCHAR(50) NULL DEFAULT NULL,
	IMG_URL			VARCHAR(200) NULL DEFAULT NULL,
	PRIMARY KEY (HOTEL_SEQ)
)ENGINE=InnoDB DEFAULT CHARSET=utf8;

CREATE TABLE IMAGE
(
    IMG_NAME  VARCHAR(50)     NOT NULL, 
    IMG_URL   VARCHAR(200)    NOT NULL, 
    PRIMARY KEY (IMG_NAME)
)ENGINE=InnoDB DEFAULT CHARSET=utf8;

CREATE TABLE MEMBER(
    MEMBER_ID 				VARCHAR(45)    NOT NULL, 
    NICKNAME      		VARCHAR(45)    NOT NULL, 
    THUMBNAIL_IMAGE     VARCHAR(200)    NOT NULL, 
    PRIMARY KEY (MEMBER_ID)
)ENGINE=InnoDB DEFAULT CHARSET=utf8;

CREATE TABLE MEMSCH (
	MS_SEQ INT(11) NOT NULL AUTO_INCREMENT,
	MS_COUNTRY_NAME VARCHAR(50) NOT NULL,
	MS_TITLE VARCHAR(50) NOT NULL,
	MEMBER_ID VARCHAR(50) NOT NULL,
	PRIMARY KEY (MS_SEQ),
	INDEX MEMBER_ID (MEMBER_ID),
	CONSTRAINT MEMBER_ID FOREIGN KEY (MEMBER_ID) REFERENCES MEMBER (MEMBER_ID)
)ENGINE=InnoDB DEFAULT CHARSET=utf8;

CREATE TABLE MEMSCHCTIY (
	MS_CTIY_SEQ INT(11) NOT NULL AUTO_INCREMENT,
	MS_DAY VARCHAR(50) NOT NULL,
	MS_CITY_NAME VARCHAR(50) NOT NULL,
	MS_DATE VARCHAR(50) NOT NULL,
	MS_SEQ INT(11) NOT NULL,
	PRIMARY KEY (MS_CTIY_SEQ),
	INDEX MS_SEQ (MS_SEQ),
	CONSTRAINT MS_SEQ FOREIGN KEY (MS_SEQ) REFERENCES MEMSCH (MS_SEQ)
)ENGINE=InnoDB DEFAULT CHARSET=utf8;

CREATE TABLE MEMSCHATTR (
	MS_ATTR_SEQ INT(11) NOT NULL AUTO_INCREMENT,
	MS_ATTR_NAME VARCHAR(50) NOT NULL,
	MS_CTIY_SEQ INT(11) NOT NULL,
	PRIMARY KEY (MS_ATTR_SEQ),
	INDEX MS_CTIY_SEQ (MS_CTIY_SEQ),
	CONSTRAINT MS_CTIY_SEQ FOREIGN KEY (MS_CTIY_SEQ) REFERENCES MEMSCHCTIY (MS_CTIY_SEQ)
)ENGINE=InnoDB DEFAULT CHARSET=utf8;


